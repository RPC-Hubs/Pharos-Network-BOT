function centerText(text, width = null) {
    if (width === null) {
        try {
            width = process.stdout.columns || 80;
        } catch {
            width = 80;
        }
    }
    if (text.length >= width) return text;
    const pad = Math.floor((width - text.length) / 2);
    return ' '.repeat(pad) + text;
}

